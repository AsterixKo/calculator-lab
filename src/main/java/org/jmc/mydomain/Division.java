package org.jmc.mydomain;

public class Division {
    public static double NumerosReales(double a, double b){
        double division = a/b;
        return division;
    }
    public static int NumerosEnteros(int a, int b){
        int division = a/b;
        return division;
    }
    public static double NumeroReal(double a){
        double inverso = 1 / a;
        return inverso;
    }
}
